// 2. Read and print an integer.
#include <stdio.h>
int main()
{
    int a;
    printf("Enter an Integer Number \n");
    scanf(" %d ", &a);
    printf("Integer Number is %d", a);
    return 0;
}

// 3. Read and print a float.
#include <stdio.h>
int main()
{
    float a;
    printf("Enter a Float Number \n");
    scanf(" %f ", &a);
    printf("Float Number is %.2f", a);
    return 0;
}